def export_forward_simple_cnn(input):
    """
    ONNX-compatible forward for simple_cnn
    Consistent with training architecture
    """
    B, mel_bins, num_frames = input.size()

    # (B, mel, time) → (B, C, mel, time)
    x = input.view(B, model.in_channel, mel_bins, num_frames)

    # -------- Block 1 --------
    x = F.relu(model.bn1(model.conv1(x)))
    x = model.drop1(x)
    x = F.max_pool2d(x, kernel_size=(1, 2))

    # -------- Block 2 --------
    x = F.relu(model.bn2(model.conv2(x)))
    x = model.drop2(x)
    x = F.max_pool2d(x, kernel_size=(1, 2))

    # -------- Block 3 --------
    x = F.relu(model.bn3(model.conv3(x)))
    x = model.drop3(x)

    # -------- Global Average Pooling --------
    x = model.gap(x)          # (B, 64, 1, 1)
    x = x.view(B, -1)         # (B, 64)

    # -------- Classifier --------
    output = model.classifier(x)  # KHÔNG softmax (đúng cho edge / ONNX)

    return output


