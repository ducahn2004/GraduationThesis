def test_tflite_accuracy(tflite_path, data_loader):
    interpreter = tf.lite.Interpreter(model_path=str(tflite_path))
    interpreter.allocate_tensors()

    input_details = interpreter.get_input_details()
    output_details = interpreter.get_output_details()

    _, T, F = input_details[0]['shape']  # (1, time, freq)

    print(f"Testing model: {tflite_path.name}")
    print(f"Expected input shape: (1, {T}, {F})")

    correct, total = 0, 0

    for features, labels in data_loader:
        for i in range(features.shape[0]):
            x = features[i].numpy()  # (freq, time) hoặc (time, freq)

            # Chuẩn hóa về (time, freq)
            if x.shape == (F, T):
                x = x.T
            elif x.shape != (T, F):
                raise ValueError(
                    f"Feature shape mismatch: got {x.shape}, expected {(T, F)}"
                )

            #x = np.expand_dims(x, axis=0).astype(np.float32)
            #interpreter.set_tensor(input_details[0]['index'], x)
            scale, zero_point = input_details[0]['quantization']
            if input_details[0]['dtype'] == np.int8:
                x_input = (x / scale + zero_point).astype(np.int8)
            else:
                x_input = x.astype(np.float32)

            x_input = np.expand_dims(x_input, axis=0)

            interpreter.set_tensor(input_details[0]['index'], x_input)

            interpreter.invoke()
            output = interpreter.get_tensor(output_details[0]['index'])

            pred = np.argmax(output, axis=1)[0]
            if pred == labels[i].item():
                correct += 1
            total += 1

    acc = correct / total
    print(f"=> Accuracy: {acc*100:.2f}% ({correct}/{total})")
    return acc

