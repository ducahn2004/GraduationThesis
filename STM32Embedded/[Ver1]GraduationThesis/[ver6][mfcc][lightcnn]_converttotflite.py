# ================== LOGMEL DATASET ==================
EPS = 1e-8

def standard_normal_variate(data):
    mean = np.mean(data)
    std = np.std(data) + EPS
    return (data - mean) / std

class MFCCDataset(Dataset):
    def __init__(
        self,
        features_h5_path,
        label_csv_path,
        keys=None,
        norm=True,
        duration=5,
        hop_length=15,
        training=False
    ):
        self.features_h5 = h5py.File(features_h5_path, 'r')
        self.labels_df = pd.read_csv(label_csv_path)

        self.keys = list(self.features_h5.keys()) if keys is None else keys
        self.key_to_label = dict(
            zip(
                self.labels_df.iloc[:, 0].astype(str),
                self.labels_df.iloc[:, 1]
            )
        )

        self.norm = norm
        self.training = training
        self.fixed_length = int(duration * 1000 / hop_length)  # 333

    def __len__(self):
        return len(self.keys)

    def __getitem__(self, idx):
        key = self.keys[idx]

        # MFCC gốc: (13, T)
        mfcc = self.features_h5[key][()].astype(np.float32)

        if self.norm:
            mfcc = standard_normal_variate(mfcc)

        n_mfcc, T = mfcc.shape

        # crop / pad time axis
        if T >= self.fixed_length:
            start = (T - self.fixed_length) // 2
            mfcc = mfcc[:, start:start + self.fixed_length]
        else:
            pad = self.fixed_length - T
            mfcc = np.pad(mfcc, ((0, 0), (0, pad)), mode="constant")

        # FINAL SHAPE: (13, 333)
        feature_tensor = torch.from_numpy(mfcc)
        label = torch.tensor(self.key_to_label.get(key, 0), dtype=torch.long)

        return feature_tensor, label

    def close(self):
        self.features_h5.close()

